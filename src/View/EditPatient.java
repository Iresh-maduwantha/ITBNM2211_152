    private void SearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBtnActionPerformed
        // TODO add your handling code here:
        ResultSet rs = new DBSearch().SearchPatient(PNICTF.getText());

        try { 
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this,"Patient Doesn't exist");
            }
            else{               
                FNameTF.setText(rs.getString("FirstName"));
                NewNICTF.setText(rs.getString("NIC"));
                LNameTF.setText(rs.getString("LastName"));
                PBirthTF.setText(rs.getString("BirthDay"));
                GenderTF.setText(rs.getString("Gender"));
                BTypeTF.setText(rs.getString("BloodType"));
                AddressTF.setText(rs.getString("Address"));
                int tele=rs.getInt("Contact");
                PContactTF.setText(String.valueOf(tele));               
            }    
        } 
        catch (SQLException ex) {
        }
    }//GEN-LAST:event_SearchBtnActionPerformed

