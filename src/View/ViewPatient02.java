    private void VHBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VHBtnActionPerformed
        // TODO add your handling code here:
        ResultSet r = new DBSearch().SearchHistory(PNICTF.getText());
        try{
            ViewPatientHistory view = new ViewPatientHistory();

            view.NameTF.setText(PFNameTF.getText());
            view.NICTF.setText(PNICTF.getText());
            DefaultTableModel dtm = (DefaultTableModel)view.ViewHistoryTable.getModel();
            dtm.setRowCount(0);

            Vector v;
            while (r.next()){
                v = new Vector();
                v.add(r.getString("Clinic"));
                v.add(r.getString("Date"));
                v.add(r.getString("Time"));
                v.add(r.getString("Amount"));
                dtm.addRow(v);
            }
            view.setVisible(true);
            //super.dispose();
        }
        catch(SQLException e){

        }
    }//GEN-LAST:event_VHBtnActionPerformed

