    private void SearchPBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchPBtnActionPerformed
        // TODO add your handling code here:
        ResultSet rs = new DBSearch().SearchPatient(SearchNIC1.getText());

        try { 
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this,"Patient Doesn't exist");
            }
            else{
                ViewPatient02 view = new ViewPatient02();
                
                view.PFNameTF.setText(rs.getString("FirstName"));
                view.PLNameTF.setText(rs.getString("LastName"));
                view.PNICTF.setText(rs.getString("NIC"));
                view.PBirthTF.setText(rs.getString("BirthDay"));
                view.GenderTF.setText(rs.getString("Gender"));
                view.BTypeTF.setText(rs.getString("BloodType"));
                view.AddressTF.setText(rs.getString("Address"));
                int tele=rs.getInt("Contact");
                view.PContactTF.setText(String.valueOf(tele));
                
                view.setVisible(true);
            }
            
        } catch (SQLException ex) {
        }
        
    }//GEN-LAST:event_SearchPBtnActionPerformed

