    private void SearchHBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchHBtnActionPerformed
        // TODO add your handling code here:
        ResultSet rs = new DBSearch().SearchHistory(SearchNIC2.getText());
        try{
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this,"Patient has no history");
            }
            else{
                ViewPatientHistory view = new ViewPatientHistory();
                
                view.NameTF.setText(rs.getString("Name"));
                view.NICTF.setText(rs.getString("NIC"));
                DefaultTableModel dtm = (DefaultTableModel)view.ViewHistoryTable.getModel();
                dtm.setRowCount(0);
            
                Vector v;
                while (rs.next()){
                    v = new Vector();
                    v.add(rs.getString("Clinic"));
                    v.add(rs.getString("Date"));
                    v.add(rs.getString("Time"));
                    v.add(rs.getString("Amount"));
                    dtm.addRow(v);
                }
                view.setVisible(true);
            }
        }
        catch(SQLException e){

        }
    }//GEN-LAST:event_SearchHBtnActionPerformed

    private void SearchCBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchCBtnActionPerformed
        // TODO add your handling code here:
        String Clinic = PClinicCMB.getSelectedItem().toString();
        
        ResultSet rs = new DBSearch().SearchClinic(Clinic);
        
        try{
            ViewClinicWise view = new ViewClinicWise();
                

            DefaultTableModel dtm = (DefaultTableModel)view.ViewClinicTB.getModel();
            dtm.setRowCount(0);
            
            Vector v;
            while (rs.next()){
                v = new Vector();
                v.add(rs.getString("Name"));
                v.add(rs.getString("NIC"));                
                v.add(rs.getString("Clinic"));
                v.add(rs.getString("Date"));
                v.add(rs.getString("Time"));

                dtm.addRow(v);
            }
            view.setVisible(true);
        }
        catch(SQLException e){

        }
        
    }//GEN-LAST:event_SearchCBtnActionPerformed

